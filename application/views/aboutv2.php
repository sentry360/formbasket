<p>
    Your simple form helper.
</p>
<p>
    Let's face it, laying out forms in a clean professional manner can easily become a serious pain. In comes FormBasket, a simple CSS framework to keep form layout easy and simple.
</p>

<p>
    <strong>Cool, but does it play well with others?</strong>
</p>
<p>
    Well it all depends on who you let it play with. At the moment we only support Bootstrap, Dojo, and jQuery UI.
    I have no intention to make it function with any other UI framework.
    Though that doesn't mean you shouldn't do it, feel free to fork it and plug away.
</p>